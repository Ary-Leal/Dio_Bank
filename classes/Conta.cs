using System;
using System.Collections.Generic;
using System.Text;
using Dio_Bank.Enum;

namespace Dio_Bank.Classes
{
    public class Conta
    {
        private TipoConta TipoConta { get; set; }

        private double Saldo { get; set; }

        private double Credito { get; set; }

        private string Nome { get; set; }

        // constructor

        public Conta(TipoConta tipoConta, double saldo, double credito, string nome, TipoConta TipoConta)
        {

            TipoConta = tipoConta;
            Saldo = saldo;
            Credito = credito;
            Nome = nome;
        }
        public bool Sacar(double ValorSaque)
        {
            //validacao do saldo insuficiente

            if (Saldo - ValorSaque < Credito * -1)
            {
                Console.WriteLine("Saldo insuficiente.");
                return false;
            }
            Saldo -= ValorSaque;
            Console.WriteLine("saldo atual da conta e {0} e {1}", Nome, Saldo);
            return true;
        }
        public void Depositar(double valorDeposito)
        {
            Saldo += valorDeposito;
            Console.WriteLine("saldo atual da conta e {0} e {1}", Nome, Saldo);

        }
        public void Transferir(double valorTransferencia, Conta contaDestino)
        {
            if (Sacar(valorTransferencia))
            {
                contaDestino.Depositar(valorTransferencia);
            }

        }
        public override string ToString()
        {
            string retorno = "";

            retorno += "TipoConta" + TipoConta + "|";
            retorno += "Nome" + Nome + "|";
            retorno += " Saldo" + Saldo + "|";
            retorno += "Credito" + Credito + "|";
            return retorno;
        }
    }


}











































































































































































































































































































































































































































































































































































































































































































































